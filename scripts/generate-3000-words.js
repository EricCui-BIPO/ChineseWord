// 生成汉字数据
function generateWords() {
  const words = [];
  const uniqueChars = [...new Set(commonCharacters.split(''))]; // 去重
  const charCount = Math.min(uniqueChars.length, 3000); // 最多3000个
  
  for (let i = 0; i < charCount; i++) {
    const char = uniqueChars[i];
    const id = `word-${String(i + 1).padStart(4, '0')}`;
    
    // 根据字符位置分配难度和分类
    let category = '一年级';
    let difficulty = 1;
    
    if (i < 500) {
      category = '一年级';
      difficulty = 1;
    } else if (i < 1000) {
      category = '二年级';
      difficulty = 2;
    } else if (i < 1500) {
      category = '三年级';
      difficulty = 2;
    } else if (i < 2000) {
      category = '四年级';
      difficulty = 3;
    } else if (i < 2500) {
      category = '五年级';
      difficulty = 3;
    } else {
      category = '六年级';
      difficulty = 4;
    }
    
    // 生成基础拼音（这里使用占位符，实际应该从字典获取）
    const pinyin = generatePinyin(char);
    
    // 生成基础意思（这里使用占位符，实际应该从字典获取）
    const meaning = generateMeaning(char);
    
    // 生成组词示例
    const words = generateWordExamples(char);
    
    words.push({
      id,
      character: char,
      pinyin,
      meaning,
      words,
      category,
      difficulty
    });
  }
  
  return words;
}

// 简化的拼音生成（实际应该使用拼音转换库或字典API）
function generatePinyin(char) {
  // 这里只是占位符，实际应该使用拼音转换库
  // 可以使用 pinyin-pro 或类似的库
  return 'pinyin'; // 占位符
}

// 简化的意思生成（实际应该从字典获取）
function generateMeaning(char) {
  // 这里只是占位符，实际应该从字典API获取
  return `${char}的意思`; // 占位符
}

// 生成组词示例
function generateWordExamples(char) {
  // 根据常见组词模式生成
  return [`${char}字`, `${char}词`, `用${char}`];
}

