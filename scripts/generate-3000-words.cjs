// 生成组词
function getWordExamples(char) {
  return [`${char}字`, `${char}词`, `用${char}`];
}

// 生成数据的函数
function generateWordData(char, id) {
  const index = id - 1;
  let category = '一年级';
  let difficulty = 1;
  
  if (index > 500) { category = '二年级'; difficulty = 2; }
  if (index > 1000) { category = '三年级'; difficulty = 2; }
  if (index > 1500) { category = '四年级'; difficulty = 3; }
  if (index > 2000) { category = '五年级'; difficulty = 3; }
  if (index > 2500) { category = '六年级'; difficulty = 4; }
  
  return {
    id: `word-${String(id).padStart(4, '0')}`,
    character: char,
    pinyin: getPinyin(char),
    meaning: getMeaning(char),
    words: getWordExamples(char),
    category: category,
    difficulty: difficulty
  };
}

