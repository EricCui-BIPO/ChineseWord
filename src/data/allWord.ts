interface WordData {
  id: string
  character: string
  pinyin: string
  meaning: string
  words: string[]
  category: string
  difficulty: number
}

/**
 * 根据字符和年级生成 Word 对象
 * @param character 汉字
 * @param gradeIndex 年级索引（0=一年级, 1=二年级, 等）
 * @param wordIndex 在该年级中的索引
 * @returns Word 对象
 */
export function generateWordData(
  character: string,
  gradeIndex: number,
  wordIndex: number
): WordData {
  const gradeNames = ['一年级', '二年级', '三年级', '四年级', '五年级']
  const wordCount = [GRADE_ONE.length, GRADE_TWO.length, GRADE_THREE.length, GRADE_FOUR.length, GRADE_FIVE.length]

  // 计算总ID（全局索引）
  let totalIndex = wordIndex
  for (let i = 0; i < gradeIndex; i++) {
    totalIndex += wordCount[i]
  }

  const pinyin = characterPinyinMap[character] || 'pīn yīn'
  const difficulty = gradeIndex + 1

  return {
    id: `word-${String(totalIndex + 1).padStart(4, '0')}`,
    character,
    pinyin,
    meaning: `${character}的意思`,
    words: [`${character}字`, `${character}词`, `用${character}`],
    category: gradeNames[gradeIndex],
    difficulty
  }
}

/**
 * 将所有年级的字符整合成 words.json 格式的数组
 * @returns 整合后的 Word 数组
 */
export function mergeAllGradesData(): WordData[] {
  const allGrades = [GRADE_ONE, GRADE_TWO, GRADE_THREE, GRADE_FOUR, GRADE_FIVE]
  const result: WordData[] = []

  allGrades.forEach((gradeChars, gradeIndex) => {
    gradeChars.forEach((character, wordIndex) => {
      result.push(generateWordData(character, gradeIndex, wordIndex))
    })
  })

  return result
}

/**
 * 导出整合数据统计信息
 */
export function getDataSummary() {
  const allData = mergeAllGradesData()
  const gradeNames = ['一年级', '二年级', '三年级', '四年级', '五年级']
  const gradeCounts = [GRADE_ONE.length, GRADE_TWO.length, GRADE_THREE.length, GRADE_FOUR.length, GRADE_FIVE.length]

  return {
    totalCharacters: allData.length,
    byGrade: gradeNames.map((name, index) => ({
      grade: name,
      count: gradeCounts[index]
    })),
    idRange: `word-0001 至 word-${String(allData.length).padStart(4, '0')}`
  }
}



