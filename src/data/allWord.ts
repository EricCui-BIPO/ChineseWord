/**
 * 检验数组中是否有重复元素
 * @param arr 要检验的字符串数组
 * @param arrayName 数组名称（用于输出信息）
 * @returns 包含检验结果的对象
 */
export function checkDuplicates(arr: string[], arrayName: string = 'Array') {
  const seen = new Map<string, number[]>()
  const duplicates: { char: string; indices: number[] }[] = []

  // 记录每个字符出现的位置
  arr.forEach((char, index) => {
    if (seen.has(char)) {
      seen.get(char)!.push(index)
    } else {
      seen.set(char, [index])
    }
  })

  // 找出重复的字符
  seen.forEach((indices, char) => {
    if (indices.length > 1) {
      duplicates.push({ char, indices })
    }
  })

  // 返回检验结果
  return {
    hasDuplicates: duplicates.length > 0,
    total: arr.length,
    unique: seen.size,
    duplicates,
    getMessage: () => {
      if (duplicates.length === 0) {
        return `✓ ${arrayName}: 无重复，共 ${arr.length} 个字符`
      }
      const details = duplicates
        .map(d => `  "${d.char}" 出现 ${d.indices.length} 次，位置: [${d.indices.join(', ')}]`)
        .join('\n')
      return `✗ ${arrayName}: 发现 ${duplicates.length} 个重复字符\n${details}`
    }
  }
}

